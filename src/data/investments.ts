  INVESTMENT_CATALOG.reduce(
    (acc, item) => {
      acc[item.id] = item
      return acc
    },
    {} as Record<InvestmentCategory, InvestmentCatalogItem>,
  )

export function getCatalogItem(id: InvestmentCategory): InvestmentCatalogItem {
  return CATALOG_BY_ID[id]
}

